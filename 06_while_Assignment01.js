// WAP to print numbers from 5 to 15 by incrementing 1


console.log("1) WAP to print numbers from 5 to 15");
let i=5; // initialization i = 1   2  3
while (i<=15) { // condition 0<=15
   console.log(i); // 0  1 2
   i = i + 1;
}



console.log("----------------------------------------------------------------------------");



console.log("2) WAP to print numbers from 50 to 40");



let k = 50; 
while (k>=40) {
   console.log(k);
   k--;
}


